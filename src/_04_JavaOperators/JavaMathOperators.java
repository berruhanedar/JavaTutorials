package _04_JavaOperators;

public class JavaMathOperators {

    public static void main(String[] args) {
        int x = 5;
        int y = 3;

        // Addition
        System.out.println(x + y);

        // Substraction
        System.out.println(x - y);

        // Multiplication
        System.out.println(x * y);

        // Division
        System.out.println(x / y);

        // Modulus
        System.out.println(x % y);

        // Increment
        int z = 7;
        ++z;
        System.out.println(z);

        // Decrement
        int v = 7;
        --v;
        System.out.println(v);
    }
}
